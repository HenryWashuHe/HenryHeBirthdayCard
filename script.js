function showContactModal() {
    const modal = document.getElementById("contactModal");
    if (modal) {
      modal.style.display = "flex";
    }
  }
  
  function hideContactModal() {
    const modal = document.getElementById("contactModal");
    if (modal) {
      modal.style.display = "none";
    }
  }
